#include "../inc/libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*byte;

	byte = (char *) s;
	i = 0;
	while (i < n)
	{
		byte[i] = '\0';
		i++;
	}
}
